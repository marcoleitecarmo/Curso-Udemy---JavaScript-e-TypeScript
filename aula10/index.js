/**
Operadores Aritméticos
+ = Adição e Concatenação em String
- = subtração
* = mutiplicação
** = potenciação
/ = divisão
% = resto da divisão


*/

const num1 = 5;
const num2 = 10;
const num3 = 3;

console.log(num1 + num2);
console.log(num1 * num2);
console.log(num1 - num2);
console.log(num2 / num1);
console.log(num3 ** num2);
console.log(num2 % num3);

